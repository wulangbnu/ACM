void init(){
    fe[0]=0;
    fe[1]=1;
    for(int i=2;i<N;i++) fe[i]=fe[i-1]+fe[i-2];
}
void doit(int k){
    printf("Fib(%d) = %lld, ",k,fe[k]);
    if(fe[k]<=0) printf("lg does not exist\n");
    else printf("lg is %.6f\n",log2(fe[k]));
    if(fe[k]<2) puts("No prime factors");
    else printf("Prime factors:%s\n",ans[k].c_str());
}
int main(){
    freopen("in.cpp","r",stdin);
//    freopen("out.txt","w",stdout);
    init();
    ll x,y;
    while(scanf("%llx %llx",&x,&y)+1){
        if(x>y||(x==0&&y==0)) return 0;

        printf("Range %lld to %lld:\n",x,y);
        bool flag=false;
        for(int i=0;i<N&&fe[i]<=y;i++){
            if(fe[i]>=x){
                doit(i);
                flag=true;
            }
        }
        if(!flag) puts("No Fibonacci numbers in the range");
        puts("");

    }
    return 0;
}
